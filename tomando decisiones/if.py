#pedir un segundo valor y calcular la suma la resta y producto de ambos

num1 = int(input("ingrese un numero:" ))

if num1 > 0: 
    num2 = int(input("ingrese un numero:"))
    print("suma:", num1 + num2)
    print("resta:", num1 - num2)
    print("producto:", num1 * num2)

